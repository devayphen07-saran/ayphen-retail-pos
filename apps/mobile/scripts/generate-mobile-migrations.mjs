#!/usr/bin/env node
/**
 * Converts drizzle-kit's generated migration files (meta/_journal.json +
 * NNNN_tag.sql) into a plain .ts module exporting the same MigrationConfig
 * shape as a string constant.
 *
 * Why this exists instead of importing the .sql file directly (the
 * documented drizzle+Expo pattern): Metro's dependency-graph resolution and
 * its babel transform of the raw .sql content don't compose reliably enough
 * in practice — the file either fails to resolve (`.sql` not a recognized
 * source extension) or gets parsed as JS and crashes on "CREATE TABLE ...".
 * A plain .ts string constant needs zero special Metro/babel config at all.
 *
 * Run after every `drizzle-kit generate`: `pnpm db:generate` already chains
 * this automatically.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const migrationsDir = path.resolve(fileURLToPath(import.meta.url), '../../src/core/sync/db/migrations');
const journalPath = path.join(migrationsDir, 'meta', '_journal.json');
const outPath = path.join(migrationsDir, 'migrations-data.ts');

const rawJournal = JSON.parse(readFileSync(journalPath, 'utf8'));

// Pick only the fields drizzle-orm's migrator MigrationConfig actually reads
// (idx, when, tag, breakpoints) — the real _journal.json also carries a
// per-entry `version` (drizzle-kit's own schema-version marker) that isn't
// part of that type, and TS's excess-property check on the literal below
// would reject it if passed through as-is.
const journal = {
  entries: rawJournal.entries.map(({ idx, when, tag, breakpoints }) => ({ idx, when, tag, breakpoints })),
};

/** @type {Record<string, string>} */
const migrations = {};
for (const entry of rawJournal.entries) {
  const sql = readFileSync(path.join(migrationsDir, `${entry.tag}.sql`), 'utf8');
  migrations[`m${String(entry.idx).padStart(4, '0')}`] = sql;
}

const out = `// AUTO-GENERATED by scripts/generate-mobile-migrations.mjs — do not hand-edit.
// Regenerate after schema changes: pnpm db:generate (chains drizzle-kit + this script).

export interface MobileMigrationConfig {
  journal: {
    entries: { idx: number; when: number; tag: string; breakpoints: boolean }[];
  };
  migrations: Record<string, string>;
}

export const migrations: MobileMigrationConfig = ${JSON.stringify({ journal, migrations }, null, 2)};
`;

writeFileSync(outPath, out);
console.log(`Wrote ${outPath}`);
